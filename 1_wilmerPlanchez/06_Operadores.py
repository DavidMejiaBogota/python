#Operadores

#Operadores Aritméticos
#Se utlizan para raelizar operaciones aritméticas como sumar, restar, multiplicar, exponente, dividir y módulo 

#Operadores aritméticos:

# Símbolo   Significado     Ejemplo             Resultado
# +         Suma            a = 10 + 5          a es 15
# -         Resta           a = 12 - 7          a es 5
# *         Multiplicación  a = 7 * 5           a es 35
# **        Exponente       a = 2 ** 3          a es 8
# /         División        a = 12.5 / 2        a es 6.25
# //        División entera a = 12. // 2        a es 6
# %         Módulo          a = 7 / 2           a es 1
___________________________________________________________

#Operadores Condicionales o de Comparación

# Símbolo   Significado     Ejemplo             Resultado
# ==        Igual que       5 == 7              Falso
# !=        Distinto que    Rojo != Verde       Verdadero
# <         Menor que       8 < 12              Verdadero
# >         Mayor que       12 > 7              Falso
# <=        Menor igual que 12 <= 12            Verdadero
# >=        Mayor igual que 4 >= 5              Falso
____________________________________________________________

